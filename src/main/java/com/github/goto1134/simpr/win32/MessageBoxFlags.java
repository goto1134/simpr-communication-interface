public enum MessageBoxFlags {

    OK(0x00000000L),
    OK_CANCEL(0x00000001L),
    ABORT_RETRY_IGNORE(0x00000002L),
    YES_NO_CANCEL(0x00000003L),
    YES_NO(0x00000004L),
    RETRY_CANCEL(0x00000005L),
    CANCEL_TRY_CONTINUE(0x00000006L),


    ICON_HAND(0x00000010L),
    ICON_QUESTION(0x00000020L),
    ICON_EXCLAMATION(0x00000030L),
    ICON_ASTERISK(0x00000040L),

    USER_ICON(0x00000080L),
    ICON_WARNING(ICON_EXCLAMATION.value),
    ICON_ERROR(ICON_HAND.value),

    ICON_INFORMATION(ICON_ASTERISK.value),
    ICON_STOP(ICON_HAND.value),

    DEF_BUTTON1(0x00000000L),
    DEF_BUTTON2(0x00000100L),
    DEF_BUTTON3(0x00000200L),
    DEF_BUTTON4(0x00000300L),

    APPL_MODAL(0x00000000L),
    SYSTEM_MODAL(0x00001000L),
    TASK_MODAL(0x00002000L),
    HELP_BUTTON(0x00004000L),

    NO_FOCUS(0x00008000L),
    SET_FOREGROUND(0x00010000L),
    DEFAULT_DESKTOP_ONLY(0x00020000L),

    TOPMOST(0x00040000L),
    RIGHT(0x00080000L),
    RTL_READING(0x00100000L),
    MB_SERVICE_NOTIFICATION(0x00200000L),
    /**
     * Use when _WIN32_WINNT < 0x0400
     */
    MB_SERVICE_NOTIFICATION_OLD(0x00040000L),
    MB_SERVICE_NOTIFICATION_NT3X(0x00040000L);

    private final long value;

    MessageBoxFlags(long value) {this.value = value;}

    public long longValue() {
        return value;
    }
}
